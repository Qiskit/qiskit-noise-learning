"""FidelityModel"""

from abc import ABC, abstractmethod
from collections.abc import Hashable
from itertools import chain
from typing import Generic, TypeVar

from qiskit_noise_learning.gate_sets import GateSet, ModelGateSet
from qiskit_noise_learning.math import IndexedVector
from qiskit_noise_learning.sequences import FidelityIndex, Path, PathPattern

ParameterIndex = TypeVar("ParameterIndex", bound=Hashable)


class FidelityModel(Generic[ParameterIndex], ABC):
    r"""A linear parameterization of the log fidelities of a gate set.

    This class provides an interface into rows of the matrix :math:`A` for which
    :math:`-\log(f) = Ar`, where :math:`f` is the vector of fidelities, and :math:`r` is a reduced
    parameter space.

    Args:
        gate_set: The gate set for which the fidelities are modelled, to be converted to a
            :class:`ModelGateSet`.
    """

    def __init__(self, gate_set: GateSet):
        self._gate_set = gate_set.model_gate_set

    @property
    def gate_set(self) -> ModelGateSet:
        return self._gate_set

    def multiplicative_row_from_path_pattern(
        self, path_pattern: PathPattern
    ) -> IndexedVector[ParameterIndex]:
        """Get the multiplicative design matrix row for the repeatable fragment of the path pattern.

        Args:
            path_pattern: The path pattern.
        """
        vector = IndexedVector[ParameterIndex](dict())

        for fidelity_index in path_pattern.repeatable_fragment:
            new_row = self.row_from_fidelity(fidelity_index)
            vector += new_row

        return vector

    @abstractmethod
    def row_from_fidelity(self, fidelity_index: FidelityIndex) -> IndexedVector[ParameterIndex]:
        """Get a row of the fidelity parameterization matrix.

        Args:
            fidelity_index: The fidelity index for the row.
        """

    def row_from_path(self, path: Path) -> IndexedVector[ParameterIndex]:
        """Get the design matrix row generated by this path."""

        # start with repeatable fragment
        vector = path.depth * self.multiplicative_row_from_path_pattern(path.pattern)

        for fidelity_index in chain(path.pattern.start_fragment, path.pattern.end_fragment):
            vector += self.row_from_fidelity(fidelity_index)

        return vector
